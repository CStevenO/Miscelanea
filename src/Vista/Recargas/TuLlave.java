    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if(btnActualizar.getText().equals("Actualizar")){
            try {
                btnActualizar.setText("Cargar");
                btnEliminar.setEnabled(false);
                btnConsultar.setEnabled(false);
                Date sqlDate = new Date(txtFecha.getDateFormat().parse(txtFecha.getText()).getTime());
                MTuLlave recarga = new CLlave().Consultar(sqlDate);
                recarga.calcular();
                txtRecargas.setText(recarga.getRecargas()+"");
                txtVentas.setText(recarga.getVentas()+"");
                txtSaldo.setText(recarga.getSaldo()+"");
                txtCaja.setText(recarga.getCaja()+"");
            } catch (ParseException ex) {
                Logger.getLogger(TuRed.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            try {
                btnActualizar.setText("Actualizar");
                btnEliminar.setEnabled(true);
                btnConsultar.setEnabled(true);
                MTuLlave recarga = new MTuLlave();
                Date sqlDate = new Date(txtFecha.getDateFormat().parse(txtFecha.getText()).getTime());
                recarga.setFecha(sqlDate);
                recarga.setRecargas(Long.parseLong(txtRecargas.getText()));
                recarga.setVentas(Long.parseLong(txtVentas.getText()));
                recarga.setSaldo(Long.parseLong(txtSaldo.getText()));
                recarga.setCaja(Long.parseLong(txtCaja.getText()));
                recarga.calcular();
                boolean reca = new CLlave().Actualizar(recarga);
                Date actual = new Date(Calendar.getInstance().getTime().getTime());
                List<MTuLlave> todos = new CLlave().ConsultarRango(recarga.getFecha(), actual);
                int i=0;
                while(i<todos.size()){
                    if(i==0){
                        todos.get(i).calcular();
                    }
                    else{
                        todos.get(i).calcular(todos.get(i-1));
                    }
                    i++;
                }
                boolean reca2 = new CLlave().Actualizar(todos);
                reca = reca||reca2;
                String mensaje = reca?"Tu Llave Registrado":"Tu Llave NO Registrado";
                limpiar();
                JOptionPane.showMessageDialog(this,mensaje);
            } catch (ParseException ex) {
                Logger.getLogger(TuRed.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

