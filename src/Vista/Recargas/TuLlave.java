    private void btnRecargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRecargarActionPerformed
        try {
            Date sqlDate = new Date(txtFecha.getDateFormat().parse(txtFecha.getText()).getTime());
            boolean reca = new MTuLlave().recargar(sqlDate, Long.parseLong(txtRecargar.getText()));
            String mensaje = reca?"Saldo agregado":"Algo sucedio, por favor revise los datos de las tablas soluciones y tu llave o revisé sí la transacción se realizó";
            JOptionPane.showMessageDialog(this,mensaje);
            txtRecargar.setText(null);
            btnRecargar.setEnabled(false);
        } catch (ParseException ex) {
            Logger.getLogger(TuRed.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRecargarActionPerformed

    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        try {
            MTuLlave recarga = new MTuLlave();
            Date sqlDate = new Date(txtFecha.getDateFormat().parse(txtFecha.getText()).getTime());
            recarga.setFecha(sqlDate);
            recarga.setRecargas(Long.parseLong(txtRecargas.getText()));
            recarga.setVentas(Long.parseLong(txtVentas.getText()));
            recarga.setSaldo(Long.parseLong(txtSaldo.getText()));
            recarga.setCaja(Long.parseLong(txtCaja.getText()));
            recarga.calcular();
            boolean reca = new CLlave().Insertar(recarga);
            String mensaje = reca?"Tu Llave Registrado":"Tu Llave NO Registrado";
            limpiar();
            JOptionPane.showMessageDialog(this,mensaje);
            btnIngresar.setEnabled(false);
        } catch (ParseException ex) {
            Logger.getLogger(TuRed.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

